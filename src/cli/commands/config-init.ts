// ---------------------------------------------------------------------------
// Command implementation
// ---------------------------------------------------------------------------

/**
 * Generate a default TOML config file with comments.
 * Returns 0 on success, 1 on error.
 */
export async function configInitCommand(args: string[]): Promise<number> {
  let options: ConfigInitOptions;
  try {
    options = parseConfigInitArgs(args);
  } catch (err: unknown) {
    const msg = err instanceof Error ? err.message : String(err);
    process.stderr.write(`Error: ${msg}\n`);
    return 1;
  }

  // Check if file already exists (unless --force)
  if (!options.force) {
    const file = Bun.file(options.output);
    if (await file.exists()) {
      process.stderr.write(
        `Error: File already exists: ${options.output}\nUse --force to overwrite.\n`,
      );
      return 1;
    }
  }

  const template = generateConfigTemplate(options.mode);

  try {
    await Bun.write(options.output, template);
  } catch (err: unknown) {
    const msg = err instanceof Error ? err.message : String(err);
    process.stderr.write(`Error: Failed to write config file: ${msg}\n`);
    return 1;
  }

  process.stdout.write(
    `Generated default config at ${options.output}\n` +
      `Edit this file, then run:\n` +
      `  collatr-edge config validate --config ${options.output}\n` +
      `  collatr-edge run --config ${options.output}\n`,
  );
  return 0;
}
