function generateHubSection(mode: NetworkMode): string {
  if (mode === "connected") {
    return `
# ---------------------------------------------------------------------------
# Hub Connection (Sparkplug B) — edit values for your environment
# ---------------------------------------------------------------------------
[agent.hub]
  group_id = "plant_floor"
  edge_node_id = "edge-01"
  broker = "mqtts://hub.collatr.com:8883"
  tls_cert = "@{secrets:hub_cert}"
  tls_key = "@{secrets:hub_key}"
`;
  }

  const note =
    mode === "standalone"
      ? "# Hub connection — not available in standalone mode"
      : "# Hub connection — uncomment and configure to enable Hub forwarding";

  return `
${note}
# [agent.hub]
#   group_id = "plant_floor"
#   edge_node_id = "edge-01"
#   broker = "mqtts://hub.collatr.com:8883"
#   tls_cert = "@{secrets:hub_cert}"
#   tls_key = "@{secrets:hub_key}"
`;
}

function generateNetworkPolicySection(mode: NetworkMode): string {
  switch (mode) {
    case "connected":
      return `
# ---------------------------------------------------------------------------
# Network Policy (connected — allows Hub and internet access)
# ---------------------------------------------------------------------------
[network_policy]
  mode = "connected"
`;

    case "local_network":
      return `
# ---------------------------------------------------------------------------
# Network Policy (local_network — local LAN only, no internet)
# ---------------------------------------------------------------------------
[network_policy]
  mode = "local_network"

# [network_policy.egress]
#   allow_dns = false
#   allow_mqtt_hub = false
#   allowed_hosts = ["192.168.1.200:8086"]

# [network_policy.ingress]
#   allow_local_webui = true
#   allow_local_api = true
#   allowed_cidrs = ["192.168.1.0/24"]
`;

    case "standalone":
      return `
# ---------------------------------------------------------------------------
# Network Policy (standalone — no network egress)
# ---------------------------------------------------------------------------
[network_policy]
  mode = "standalone"

# [network_policy.egress]
#   allow_dns = false
#   allow_mqtt_hub = false
#   allowed_hosts = []
`;
  }
}

