// CollatrEdge — config init command
// PRD refs: §7 Configuration, §18 Deployment, §22 Scenario 5, Appendix A

export type NetworkMode = "connected" | "local_network" | "standalone";

export interface ConfigInitOptions {
  output: string;
  mode: NetworkMode;
  force: boolean;
}

/**
 * Parse config init-specific args: --output/-o, --mode, --force.
 */
export function parseConfigInitArgs(args: string[]): ConfigInitOptions {
  let output = "./collatr-edge.toml";
  let mode: NetworkMode = "local_network";
  let force = false;

  for (let i = 0; i < args.length; i++) {
    const arg = args[i]!;
    if (arg === "--output" || arg === "-o") {
      const next = args[i + 1];
      if (next === undefined || next.startsWith("-")) {
        throw new Error("--output requires a file path argument");
      }
      output = next;
      i++;
    } else if (arg === "--mode") {
      const next = args[i + 1];
      if (
        next !== "connected" &&
        next !== "local_network" &&
        next !== "standalone"
      ) {
        throw new Error(
          `Invalid mode: ${next ?? "(missing)"}. Must be one of: connected, local_network, standalone`,
        );
      }
      mode = next;
      i++;
    } else if (arg === "--force") {
      force = true;
    }
  }

  return { output, mode, force };
}

// ---------------------------------------------------------------------------
// Config template generation
